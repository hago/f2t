package com.hagoapp.f2t.datafile;

public class DataCell<T> {
    private T data;
    private int index;

    public T getData() {
        return data;
    }

    public void setData(T data) {
        this.data = data;
    }

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }
}
